std::bitset<p + 1> ip;

void sieve()
{
    phi[1] = 1;
    ip.set(1);
    std::vector<int> vp;
    for (int i = 2; i <= p; ++i)
    {
        if (!ip.test(i))
        {
            phi[i] = i - 1;
            vp.push_back(i);
        }
        for (int v : vp)
        {
            if (i * v > p)
                break;
            ip.set(i * v);
            if (i % v)
                phi[i * v] = phi[i] * phi[v];
            else
            {
                phi[i * v] = phi[i] * v;
                break;
            }
        }
    }
}

int qpow(int x, int n, const int mod)
{
    int ret = 1;
    while (n)
    {
        if (n & 1)
            ret = 1ll * ret * x % mod;
        x = 1ll * x * x % mod;
        n /= 2;
    }
    return ret;
}

int func(int x, const int mod)
{
    if (mod ^ 1)
        return qpow(x, func(x, phi[mod]) + phi[mod], mod);
    else
        return 0;
}

